void lcd_put_ascii(int x, int y, unsigned char c,PT_PixelDatas ptPixelDatas)
{
	unsigned char *dots = (unsigned char *)&fontdata_8x16[c*16];
	int i, b;
	unsigned char byte;

	for (i = 0; i < 16; i++)
	{
		byte = dots[i];
		for (b = 7; b >= 0; b--)
		{
			if (byte & (1<<b))
			{
				/* show */
				lcd_put_pixel(x+7-b, y+i, 0xffffff,ptPixelDatas); /*  */
			}
			else
			{
				/* hide */
				lcd_put_pixel(x+7-b, y+i, 0,ptPixelDatas); /*  */
			}
		}
	}
}

void lcd_put_chinese(int x, int y, unsigned char *str,PT_PixelDatas ptPixelDatas)
{
	unsigned int area ;
	unsigned int where ;
	unsigned char *dots = NULL;
	unsigned char byte;
	unsigned char k=0;

	while(str[k]!=0)
	{

		area  = str[k] - 0xA1;
		where = str[k+1] - 0xA1;
		dots = hzkmem + (area * 94 + where)*32;


		int i, j, b;
		for (i = 0; i < 16; i++)
		for (j = 0; j < 2; j++)
		{
			byte = dots[i*2 + j];
			for (b = 7; b >=0; b--)
			{
				if (byte & (1<<b))
				{
					/* show */
					lcd_put_pixel(x+j*8+7-b, y+i, 0xffffff,ptPixelDatas); /*  */
				}
				else
				{
					/* hide */
					lcd_put_pixel(x+j*8+7-b, y+i, 0,ptPixelDatas); /*  */
				}
			}
		}
		k=k+2;
		x=x+16;
	}
}

